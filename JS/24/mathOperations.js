function sum(a, b) {
  let result = a + b;
  console.log(`${a} + ${b} = ${result}`);
}

function subtract(a, b) {
  let result = a - b;
  console.log(`${a} - ${b} = ${result}`);
}

function multiply(a, b) {
  let result = a * b;
  console.log(`${a} * ${b} = ${result}`);
}

function divide(a, b) {
  let result = a / b;
  console.log(`${a} / ${b} = ${result}`);
}

export { sum, subtract, multiply, divide };
